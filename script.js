function dictionary() {
  return translations[currentLanguage] || translations.ja;
}

function updateBackgroundToggle() {
  const isPlaying = !backgroundVideo.paused;
  const label = dictionary()[isPlaying ? "backgroundPause" : "backgroundPlay"];
  backgroundToggle.setAttribute("aria-label", label);
  backgroundToggle.title = label;
  backgroundToggle.querySelector("span").textContent = isPlaying ? "❚❚" : "▶";
}

function updateMusicText() {
  const copy = dictionary();
  const stateKeys = { ready: "musicReady", playing: "musicPlaying", paused: "musicPaused", blocked: "musicBlocked" };
  const isPlaying = musicState === "playing";
  const toggleLabel = copy[isPlaying ? "musicPause" : "musicPlay"];

  musicDockToggle.setAttribute("aria-label", copy[musicDock.classList.contains("is-open") ? "musicClose" : "musicOpen"]);
  musicPrevious.setAttribute("aria-label", copy.musicPrevious);
  musicToggle.setAttribute("aria-label", toggleLabel);
  musicNext.setAttribute("aria-label", copy.musicNext);
  musicVolume.setAttribute("aria-label", copy.musicVolume);
  musicStatus.textContent = copy[stateKeys[musicState]];
}

function setMusicState(state) {
  musicState = state;
  musicToggle.querySelector("span").textContent = state === "playing" ? "❚❚" : "▶";
  updateMusicText();
}

function playMusic() {
  const attempt = music.play();
  if (attempt) {
    attempt.catch(() => setMusicState("blocked"));
  }
}

function changeTrack(offset) {
  currentTrack = (currentTrack + offset + tracks.length) % tracks.length;
  music.src = tracks[currentTrack];
  musicTrack.textContent = `BGM ${String(currentTrack + 1).padStart(2, "0")}`;
  playMusic();
}

function setLanguage(lang) {
  currentLanguage = translations[lang] ? lang : "ja";
  const copy = dictionary();
  document.documentElement.lang = currentLanguage;
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = copy[element.dataset.i18n];
    if (value) element.textContent = value;
  });
  langButtons.forEach((button) => {
    const active = button.dataset.lang === currentLanguage;
    button.classList.toggle("is-active", active);
    button.setAttribute("aria-pressed", String(active));
  });
  const titleKey = document.body.dataset.titleKey;
  if (titleKey && copy[titleKey]) document.title = copy[titleKey];
  menuToggle.setAttribute("aria-label", copy[header.classList.contains("is-open") ? "menuClose" : "menuOpen"]);
  navigatorCharacter.setAttribute("aria-label", copy.guideLabel);
  localStorage.setItem("portfolio-language", currentLanguage);
  updateBackgroundToggle();
  updateMusicText();
}

function closeNavigation() {
  header.classList.remove("is-open");
  menuToggle.setAttribute("aria-expanded", "false");
  navGroups.forEach((group) => group.classList.remove("is-open"));
  navTriggers.forEach((trigger) => trigger.setAttribute("aria-expanded", "false"));
  menuToggle.setAttribute("aria-label", dictionary().menuOpen);
}

function markActiveNavigation() {
  const section = document.body.dataset.section;
  const activeTopLevel = header.querySelector(`[data-section="${section}"]`);
  if (activeTopLevel) activeTopLevel.classList.add("is-active");
  const currentPath = window.location.pathname.replace(/\/$/, "/index.html");
  header.querySelectorAll("a[href]").forEach((link) => {
    if (link.getAttribute("href") === currentPath) link.setAttribute("aria-current", "page");
  });
}

